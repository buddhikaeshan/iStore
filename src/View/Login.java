    public void login(){
        if (txtUname.getText().isEmpty() || txtPass.getPassword().equals("")) {
            JOptionPane.showMessageDialog(this, "Username or Password is Empty");
        } else {
            String uname = txtUname.getText();
            char[] pass = txtPass.getPassword();
            this.loginModel = new LoginUserModel(uname, pass);
            controller.Login(loginModel);
        }
    }
    
